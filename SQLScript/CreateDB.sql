CREATE DATABASE TMapDB;
GO

USE TMapDB
GO

CREATE TABLE Machines(
Machine_ID INT IDENTITY PRIMARY KEY,
Machine_Name NVARCHAR(50)
);

CREATE TABLE Coordinates(
Coordinate_ID INT IDENTITY PRIMARY KEY,
Coordinate NVARCHAR(100)
);
GO

CREATE TABLE MapPoints(
MapPoint_ID INT IDENTITY PRIMARY KEY,
Machine_ID INT FOREIGN KEY REFERENCES Machines(Machine_ID),
Coordinate_ID INT FOREIGN KEY REFERENCES Coordinates(Coordinate_ID)
);

